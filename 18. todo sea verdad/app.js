
function truthCheck(collection, pre) {
    return collection
            .map(x => x[pre])
            .every(a => Boolean(a))
}

console.log(truthCheck([{name: "Quincy", role: "Founder", isBot: false}, {name: "Naomi", role: "", isBot: false}, {name: "Camperbot", role: "Bot", isBot: true}], "isBot"))
